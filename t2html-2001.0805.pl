    }


    $LINK_CHECK = 1  if    $LINK_CHECK_ERR_TEXT_ONE_LINE;


    for ( @reference )
    {
        my $sep = $referenceSeparator || "=";
        my ( $key, $value ) = split /$sep/, $ARG;       #font-lock s/

